package com.my.spring.domain;

public class WorkerDetailVO {
	int id, id_workerInfo, fulltime, night;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getId_workerInfo() {
		return id_workerInfo;
	}

	public void setId_workerInfo(int id_workerInfo) {
		this.id_workerInfo = id_workerInfo;
	}

	public int getFulltime() {
		return fulltime;
	}

	public void setFulltime(int fulltime) {
		this.fulltime = fulltime;
	}

	public int getNight() {
		return night;
	}

	public void setNight(int night) {
		this.night = night;
	}
}